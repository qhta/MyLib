namespace Qhta.Unicode;

public enum Category
{
  Cc = 0x4363,    // Control
  Cf = 0x4366,    // Format
  Co = 0x436F,    // Private Use
  Cs = 0x4373,    // Surrogate
  Ll = 0x4C6C,    // Lowercase Letter
  Lm = 0x4C6D,    // Modifier Letter
  Lo = 0x4C6F,    // Other Letter
  Lt = 0x4C74,    // Titlecase Letter
  Lu = 0x4C75,    // Uppercase Letter
  Mc = 0x4D63,    // Spacing Mark
  Me = 0x4D65,    // Enclosing Mark
  Mn = 0x4D6E,    // Non-spacing Mark
  Nd = 0x4E64,    // Decimal Number
  Nl = 0x4E6C,    // Letter Number
  No = 0x4E6F,    // Other Number
  Pc = 0x5063,    // Connector Punctuation
  Pd = 0x5064,    // Dash Punctuation
  Pe = 0x5065,    // Close Punctuation
  Pf = 0x5066,    // Final Punctuation
  Pi = 0x5069,    // Initial Punctuation
  Po = 0x506F,    // Other Punctuation
  Ps = 0x5073,    // Open Punctuation
  Sc = 0x5363,    // Currency Symbol
  Sk = 0x536B,    // Modifier Symbol
  Sm = 0x536D,    // Math Symbol
  So = 0x536F,    // Other Symbol
  Zl = 0x5A6C,    // Line Separator
  Zp = 0x5A70,    // Paragraph Separator
  Zs = 0x5A73,    // Space Separator
}
