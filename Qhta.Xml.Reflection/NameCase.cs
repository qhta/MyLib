namespace Qhta.Xml.Reflection;

/// <summary>
/// Specifies modes for name case change.
/// </summary>
public enum NameCase
{
  /// <summary>
  /// Leave unchanged
  /// </summary>
  Unchanged,
  /// <summary>
  /// Turn first letter to lowercase
  /// </summary>
  LowercaseFirstLetter,
  /// <summary>
  /// Turn first letter to uppercase
  /// </summary>
  UppercaseFirstLetter
}