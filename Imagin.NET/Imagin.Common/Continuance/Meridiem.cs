namespace Imagin.Common
{
    /// <summary>
    /// Specifies a time before or after midday.
    /// </summary>
    public enum Meridiem
    {
        /// <summary>
        /// Specifies a time BEFORE midday.
        /// </summary>
        Ante,
        /// <summary>
        /// Specifies a time AFTER midday.
        /// </summary>
        Post,
        /// <summary>
        /// Specifies a time with unspecified meridiem.
        /// </summary>
        Unspecified
    }
}
